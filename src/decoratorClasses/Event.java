package decoratorClasses;

public class Event {

	private String action;
	private String string;
	
	public Event(String action,String string)
	{
		this.action = action;
		this.string = string;
		
	}

	public String getAction() {
		return action;
	}

	public String getString() {
		return string;
	}

	

	
	



}
